// Language switcher logic
function switchLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n").trim();
    if (translations[key] && translations[key][lang]) {
      el.innerHTML = translations[key][lang];
    }
  });
  document.getElementById("currentLang").textContent = lang.toUpperCase();
  localStorage.setItem("lang", lang);
}

// Auto-detect user language
window.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("lang");
  const userLang = savedLang || navigator.language.slice(0, 2);
  const supportedLangs = ["en", "ru", "uz"];
  const langToSet = supportedLangs.includes(userLang) ? userLang : "en";
  switchLanguage(langToSet);
});

document.addEventListener("DOMContentLoaded", () => {
  const toggle = document.getElementById("langToggle");
  const switcher = toggle.closest(".lang-switcher");
  const options = document.getElementById("langOptions");

  toggle.addEventListener("click", (e) => {
    e.stopPropagation();
    switcher.classList.toggle("open");
  });

  options.querySelectorAll("li").forEach(li => {
    li.addEventListener("click", (e) => {
      e.stopPropagation();
      const lang = li.getAttribute("data-lang");
      switchLanguage(lang);
      switcher.classList.remove("open");
    });
  });

  document.addEventListener("click", (e) => {
    if (!switcher.contains(e.target)) {
      switcher.classList.remove("open");
    }
  });
});
